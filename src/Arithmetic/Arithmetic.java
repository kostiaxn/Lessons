package Arithmetic;

public class Arithmetic {
    public static void main(String[] args) {
        double a = 1.05;
        double b = 2.54;
        double result = a + b;
        System.out.println(result);
        result = a - b;
        System.out.println(result);
        result = a * b;
        System.out.println(result);
        result = a / b;
        System.out.println(result);
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
        b++;
        System.out.println(b);
        b--;
        System.out.println(b);
        a += 5;
        System.out.println(a);
        int g = 8;
        result = g % 3;
        System.out.println(result);
    }
}
