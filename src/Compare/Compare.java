package Compare;

public class Compare {
    public static void main(String[] args) {
        double a = 1;
        double b = 5;
        System.out.println(a + " < " + b + " = " + (a < b));
        System.out.println(a + " > " + b + " = " + (a > b));
        System.out.println(a + " <= " + b + " = " + (a <= b));
        System.out.println(a + " >= " + b + " = " + (a >= b));
        System.out.println(a + " == " + b + " = " + (a == b));
        System.out.println(a + " != " + b + " = " + (a != b));
        }
    }

